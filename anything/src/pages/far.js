function randomWord(num) {
  let i = 0;
  var z = [];
  do {
    let random = Math.round(Math.random() * 300);
    z.push(db[random]);
    i++;
  } while (i < num);
  return z.join(" ");
}
export default function yeouch() {
  const [word, setWord] = useState("");
  useEffect(() => {
    const interval = setInterval(() => {
      setWord(randomWord(10210));
    }, 1);
    return () => clearInterval(interval);
  }, []);

  return (
    <div className="App" style={{ background: "black" }}>
      <p style={{ color: "green", fontFamily: "monospace" }}>{word}</p>
    </div>
  );
}
